package com.yuvraj;
public class DataTypes {
    public static void main(String[] args) throws Exception {
        System.out.println("Data Types");
        int a = 2;
        float b = 2.3f;
        double c = 2.33;
        long d = 2222222222l;
        byte e = 3;
        short f = 23;
        char g = 'x';
        char h = 88;
        System.out.println(a+"-"+b+"-"+c+"-"+d+"-"+e+"-"+f+"-"+g+"-"+h);
    }
}
